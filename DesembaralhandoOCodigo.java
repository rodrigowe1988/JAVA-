
public class DesembaralhandoOCodigo {

	public static void main(String[] args) {
		
		int x = 3;
		
		if(x > 2) {
			System.out.print("a");
		}
		x--;
		System.out.print("-");
		if(x == 2) {
		System.out.print("b c");
		}
		x = 1;
		System.out.print("-");
		if(x == 1) {
			System.out.print("d");
		}
	}
}